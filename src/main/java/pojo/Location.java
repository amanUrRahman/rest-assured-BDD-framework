package pojo;

public class Location {
    private double lat;
    private double lng;

    public double getLattitude() {
        return lat;
    }

    public void setLattitude(double lat) {
        this.lat = lat;
    }

    public double getLongitude() {
        return lng;
    }

    public void setLongitude(double lng) {
        this.lng = lng;
    }

}
